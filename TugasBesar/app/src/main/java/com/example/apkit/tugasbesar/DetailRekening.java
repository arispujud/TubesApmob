public class DetailRekening {
    public String idRekening;
    public String noRekening;
    public String namaRekening;
    public String kantorRekening;
    public String urlRekening;

    public String getIdRekening() {
        return idRekening;
    }

    public void setIdRekening(String idRekening) {
        this.idRekening = idRekening;
    }

    public String getNoRekening() {
        return noRekening;
    }

    public void setNoRekening(String noRekening) {
        this.noRekening = noRekening;
    }

    public String getNamaRekening() {
        return namaRekening;
    }

    public void setNamaRekening(String namaRekening) {
        this.namaRekening = namaRekening;
    }

    public String getKantorRekening() {
        return kantorRekening;
    }

    public void setKantorRekening(String kantorRekening) {
        this.kantorRekening = kantorRekening;
    }

    public String getUrlRekening() {
        return urlRekening;
    }

    public void setUrlRekening(String urlRekening) {
        this.urlRekening = urlRekening;
    }
}
